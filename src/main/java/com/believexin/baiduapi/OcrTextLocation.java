package com.believexin.baiduapi;

public class OcrTextLocation {

	int width;
	int height;
	int top;
	int left;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getLift() {
		return left;
	}
	public void setLift(int lift) {
		this.left = lift;
	}
}
